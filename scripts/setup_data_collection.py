"""
Data Collection Setup Wizard
==============================

Interactive one-time setup tool that configures the TfL and OpenWeatherMap
API keys required for real-world data collection.  The wizard validates each
key against a live API call before writing a ``.env`` file, so
``data_collection.py`` can be started without further configuration.

Usage::

    python scripts/setup_data_collection.py
"""

from __future__ import annotations

import logging
import os
import sys
import textwrap
import time
from dataclasses import dataclass
from pathlib import Path
from typing import Optional, Tuple

# Project root is added to sys.path so the config package is importable
# regardless of the working directory from which the script is invoked.
PROJECT_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

import requests  # noqa: E402  (imported after path fix)

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s - %(levelname)s - %(message)s",
    datefmt="%H:%M:%S",
)
logger = logging.getLogger(__name__)

# ANSI escapes are applied only when stdout is a real terminal.
_USE_COLOUR = sys.stdout.isatty()


def _colour(text: str, code: str) -> str:
    """Wrap ``text`` in an ANSI colour escape if stdout supports it."""
    if not _USE_COLOUR:
        return text
    return f"\033[{code}m{text}\033[0m"


def green(text: str)  -> str: return _colour(text, "32")
def red(text: str)    -> str: return _colour(text, "31")
def yellow(text: str) -> str: return _colour(text, "33")
def bold(text: str)   -> str: return _colour(text, "1")
def cyan(text: str)   -> str: return _colour(text, "36")


