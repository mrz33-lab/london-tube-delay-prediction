@dataclass
class APIConfig:
    """
    Collected API credentials, held here before being serialised to ``.env``.

    Separating credential storage from serialisation makes each step
    independently testable and easier to extend with additional keys.
    """

    tfl_key: Optional[str]
    weather_key: str

    def is_valid(self) -> bool:
        """
        Return True when the minimum required configuration is present.

        The TfL key is optional; the OpenWeatherMap key is mandatory.
        """
        return bool(self.weather_key)

    def to_env_content(self) -> str:
        """Serialise the configuration to ``.env`` file format with inline documentation."""
        tfl_line = (
            f"TFL_API_KEY={self.tfl_key}"
            if self.tfl_key
            else "# TFL_API_KEY=your_tfl_key_here  (optional)"
        )
        return textwrap.dedent(
            f"""\
            # ====================================================================
            # API Keys for London Underground Delay Prediction
            # Kept in .env (never committed to git) for security.
            # Generated by: python scripts/setup_data_collection.py
            # ====================================================================

            # TfL Unified API Key (optional — raises rate limit to 500 req/min)
            # Get yours at: https://api-portal.tfl.gov.uk/signup
            {tfl_line}

            # OpenWeatherMap API Key (required for weather data)
            # Get yours at: https://openweathermap.org/api  (free tier is sufficient)
            WEATHER_API_KEY={self.weather_key}
            """
        )


# ---------------------------------------------------------------------------
# Setup wizard class
# ---------------------------------------------------------------------------


class DataCollectionSetup:
    """
    Step-by-step API setup wizard.

    Distinct phases (registration guidance, key collection, validation,
    file generation) keep each step independently testable and clearly
    separated from the others.
    """

    TFL_PORTAL_URL: str = "https://api-portal.tfl.gov.uk/signup"
    OWM_API_URL: str = "https://home.openweathermap.org/users/sign_up"
    OWM_TEST_URL: str = "https://api.openweathermap.org/data/2.5/weather"

    def __init__(self) -> None:
        """Initialise the wizard and resolve the output ``.env`` path relative to project root."""
        self._project_root = PROJECT_ROOT
        self._env_path = self._project_root / ".env"
        logger.info("Setup wizard initialised — project root: %s", self._project_root)

    # ------------------------------------------------------------------ #
    # Public interface                                                     #
    # ------------------------------------------------------------------ #

    def run(self) -> None:
        """
        Execute the full setup wizard.

        Flow:
            1. Print welcome banner
            2. Check for an existing .env file
            3. Guide TfL registration (optional)
            4. Guide OpenWeatherMap registration (required)
            5. Collect and validate API keys
            6. Test both APIs with live requests
            7. Write the .env file
            8. Print next-steps instructions
        """
        self._print_banner()
        self._check_existing_env()
        self._print_tfl_guidance()
        self._print_owm_guidance()

        tfl_key = self._collect_tfl_key()
        weather_key = self._collect_weather_key()

        config = APIConfig(tfl_key=tfl_key, weather_key=weather_key)

        print()
        print(bold("Testing API connections..."))
        print()

        tfl_ok = self._test_tfl_api(tfl_key)
        weather_ok = self._test_weather_api(weather_key)

        if not weather_ok:
            print()
            print(red("  ✗ OpenWeatherMap API test failed."))
            print("    Please double-check your API key and try again.")
            print("    Note: new keys can take up to 10 minutes to activate.")
            sys.exit(1)

        self._write_env_file(config)
        self._print_next_steps(tfl_ok=tfl_ok)

    # ------------------------------------------------------------------ #
    # Wizard steps                                                         #
    # ------------------------------------------------------------------ #

    def _print_banner(self) -> None:
        """Print the welcome banner."""
        width = 62
        print()
        print(cyan("=" * width))
        print(cyan("  London Underground Delay Prediction"))
        print(cyan("  Real Data Collection Setup Wizard"))
        print(cyan("=" * width))
        print()
        print("  This wizard configures the API keys needed to collect")
        print("  real-world tube and weather data.")
        print()
        print("  This should take about 5 minutes.")
        print(cyan("=" * width))
        print()

    def _check_existing_env(self) -> None:
        """
        Warn the user if a ``.env`` file already exists and confirm before
        overwriting it to prevent silently destroying a working configuration.
        """
        if not self._env_path.exists():
            return

        print(yellow(f"  Warning: A .env file already exists at {self._env_path}"))
        print()
        response = input("  Overwrite it? [y/N]: ").strip().lower()
        if response not in ("y", "yes"):
            print()
            print("  Setup cancelled.  Your existing .env file was not modified.")
            sys.exit(0)
        print()

    def _print_tfl_guidance(self) -> None:
        """Print step-by-step TfL API registration instructions."""
        print(bold("STEP 1 — TfL Unified API Key (optional but recommended)"))
        print()
        print("  The TfL API is free and provides real-time tube status data.")
        print("  Without a key you can still collect data but at a lower rate.")
        print()
        print("  To register:")
        print(f"    1. Visit: {cyan(self.TFL_PORTAL_URL)}")
        print("    2. Click 'Register' and create a free account")
        print("    3. After email verification, go to 'Products'")
        print("    4. Subscribe to 'Unified API'")
        print("    5. Copy your Primary Key from the profile page")
        print()

    def _print_owm_guidance(self) -> None:
        """Print step-by-step OpenWeatherMap registration instructions."""
        print(bold("STEP 2 — OpenWeatherMap API Key (required)"))
        print()
        print("  OpenWeatherMap provides London weather data.")
        print("  The free tier allows 1,000 calls/day; the collector uses ~96/day.")
        print()
        print("  To register:")
        print(f"    1. Visit: {cyan(self.OWM_API_URL)}")
        print("    2. Create a free account")
        print("    3. Go to 'API keys' in your account dashboard")
        print("    4. Copy your Default API key")
        print()
        print(yellow("  Note: New API keys can take up to 10 minutes to activate."))
        print()

    def _collect_tfl_key(self) -> Optional[str]:
        """
        Prompt the user for their TfL API key.

        Empty input is accepted because the TfL key is optional.
        Whitespace padding from clipboard paste is stripped.

        Returns:
            The TfL API key string, or ``None`` if the user skipped this step.
        """
        print(bold("Enter your TfL API key"))
        print("  (Press Enter to skip — collection will work but at a lower rate limit)")
        print()
        raw = input("  TfL API Key: ").strip()
        key = raw if raw else None

        if key:
            print(green(f"  ✓ TfL key received ({len(key)} characters)"))
        else:
            print(yellow("  ─ TfL key skipped (optional)"))
        print()
        return key

    def _collect_weather_key(self) -> str:
        """
        Prompt the user for their OpenWeatherMap API key.

        Retries up to three times on empty input; this key is mandatory.

        Returns:
            A non-empty OpenWeatherMap API key string.

        Raises:
            SystemExit: If the user fails to provide a key after three attempts.
        """
        print(bold("Enter your OpenWeatherMap API key"))
        print("  (This is required — weather data cannot be collected without it)")
        print()

        for attempt in range(1, 4):
            raw = input("  OpenWeatherMap API Key: ").strip()
            if raw:
                print(green(f"  ✓ Weather key received ({len(raw)} characters)"))
                print()
                return raw
            print(red(f"  ✗ Key cannot be empty (attempt {attempt}/3)"))

        print()
        print(red("  No weather key provided after 3 attempts — exiting."))
        sys.exit(1)

    def _test_tfl_api(self, api_key: Optional[str]) -> bool:
        """
        Test the TfL API connection using a lightweight endpoint.

        ``/Line/central/Status`` is used because it is small and exercises
        the same authentication path as the full collection loop.

        Args:
            api_key: TfL API key, or ``None`` for unauthenticated access.

        Returns:
            ``True`` if the API responded successfully.
        """
        print("  Testing TfL API...", end=" ", flush=True)

        params: dict = {}
        if api_key:
            params["app_key"] = api_key

        try:
            response = requests.get(
                "https://api.tfl.gov.uk/Line/central/Status",
                params=params,
                timeout=10,
            )
            if response.status_code == 200:
                data = response.json()
                status_desc = (
                    data[0]["lineStatuses"][0]["statusSeverityDescription"]
                    if data and data[0].get("lineStatuses")
                    else "Unknown"
                )
                print(green("OK"))
                print(f"     Central line status: {status_desc}")
                return True
            else:
                print(yellow(f"HTTP {response.status_code}"))
                print(f"     TfL returned an unexpected status code.")
                return False

        except requests.exceptions.ConnectionError:
            print(red("FAILED (connection error — no internet?)"))
            return False
        except requests.exceptions.Timeout:
            print(red("FAILED (request timed out)"))
            return False
        except Exception as exc:  # noqa: BLE001
            print(red(f"FAILED ({exc})"))
            return False

    def _test_weather_api(self, api_key: str) -> bool:
        """
        Test the OpenWeatherMap API by fetching current London weather.

        Args:
            api_key: OpenWeatherMap API key.

        Returns:
            ``True`` if the API responded with valid weather data.
        """
        print("  Testing OpenWeatherMap API...", end=" ", flush=True)

        params = {
            "lat": 51.5074,
            "lon": -0.1278,
            "appid": api_key,
            "units": "metric",
        }

        try:
            response = requests.get(self.OWM_TEST_URL, params=params, timeout=10)

            if response.status_code == 200:
                data = response.json()
                temp = data["main"]["temp"]
                humidity = data["main"]["humidity"]
                rain_mm = data.get("rain", {}).get("1h", 0.0)
                print(green("OK"))
                print(
                    f"     London: {temp:.1f}°C, {humidity}% humidity, "
                    f"{rain_mm:.1f}mm rain"
                )
                return True

            elif response.status_code == 401:
                print(red("FAILED (invalid API key)"))
                print("     Please check your OpenWeatherMap key and try again.")
                return False

            else:
                print(yellow(f"HTTP {response.status_code}"))
                return False

        except requests.exceptions.ConnectionError:
            print(red("FAILED (connection error — no internet?)"))
            return False
        except requests.exceptions.Timeout:
            print(red("FAILED (request timed out)"))
            return False
        except Exception as exc:  # noqa: BLE001
            print(red(f"FAILED ({exc})"))
            return False

    def _write_env_file(self, config: APIConfig) -> None:
        """
        Write the API configuration to a ``.env`` file.

        Args:
            config: Validated ``APIConfig`` instance.
        """
        print()
        print(bold("Writing .env file..."), end=" ", flush=True)

        try:
            self._env_path.write_text(config.to_env_content(), encoding="utf-8")
            print(green("Done"))
            print(f"     Saved to: {self._env_path}")
        except OSError as exc:
            print(red(f"FAILED ({exc})"))
            print("    Please check write permissions for the project directory.")
            sys.exit(1)

    def _print_next_steps(self, tfl_ok: bool) -> None:
        """
        Print next-steps instructions so the user knows exactly how to start
        collecting data.

        Args:
            tfl_ok: Whether the TfL API test passed (affects the message shown).
        """
        print()
        print(cyan("=" * 62))
        print(bold("  Setup complete!  Here is what to do next:"))
        print(cyan("=" * 62))
        print()

        if not tfl_ok:
            print(yellow("  Note: The TfL API test failed (this is non-critical)."))
            print("  Collection will proceed using only status data;")
            print("  you may want to verify your network connection.")
            print()

        print(bold("  1. Test a single collection snapshot:"))
        print("     python data_collection.py --once")
        print()
        print(bold("  2. Start continuous collection (every 15 minutes):"))
        print("     python data_collection.py")
        print()
        print(bold("  3. Monitor progress:"))
        print("     python scripts/check_collection_progress.py")
        print()
        print(bold("  4. Check how much data you have collected:"))
        print("     python scripts/check_collection_progress.py --summary")
        print()
        print("  Data is saved to: data/tfl_merged.csv")
        print("  The main pipeline reads this file automatically.")
        print()
        print(yellow("  Recommended: collect for at least 2 weeks before retraining."))
        print(cyan("=" * 62))
        print()


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------


def main() -> None:
    """
    Entry point for the setup wizard.

    Wraps ``DataCollectionSetup.run()`` in a top-level exception handler
    so unexpected errors produce a clean message rather than a raw traceback.
    """
    try:
        setup = DataCollectionSetup()
        setup.run()
    except KeyboardInterrupt:
        print()
        print(yellow("\n  Setup interrupted by user."))
        sys.exit(0)
    except Exception as exc:
        print()
        print(red(f"  Unexpected error during setup: {exc}"))
        logger.exception("Setup wizard failed with an unhandled exception")
        sys.exit(1)


if __name__ == "__main__":
    main()
